package persistence.table.entity;

public class GameObject
{
	protected int id;
	
	protected long x;
	protected long y;
	
	
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public long getX()
	{
		return x;
	}
	public void setX(long x)
	{
		this.x = x;
	}
	public long getY()
	{
		return y;
	}
	public void setY(long y)
	{
		this.y = y;
	}
	
	
	
}
